export type UiLanguage = "zh" | "en";

export function getUiLanguage(value: string | null | undefined): UiLanguage {
    return value === "en" ? "en" : "zh";
}

/** BCP 47 locale for `Intl` / `toLocaleString` from UI language. */
export function uiLocale(lang: UiLanguage): "zh-TW" | "en-US" {
    return lang === "en" ? "en-US" : "zh-TW";
}

